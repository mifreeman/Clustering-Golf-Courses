%Overall length 
course_length = zeros(1, num_cs);
%overal varaince 
all_var = zeros(1, num_cs);


%calculates all pars variances for all courses  
for i = 1:num_cs
   %sorts hole yardages by par 
    par_3s = [];
    par_4s = [];
    par_5s = []; 
    for j = 1:21
       if courses(2,j,i) == 3
           par_3s(length(par_3s)+1) = courses(1,j,i);     
       end 
       if courses(2,j,i) == 4
           par_4s(length(par_4s)+1) = courses(1,j,i);
       end
       if courses(2,j,i) == 5
           par_5s(length(par_5s)+1) = courses(1,j,i); 
       end
    end
   %calculates variance by par 
   var_3(i) = var(par_3s);
   var_4(i) = var(par_4s);
   var_5(i) = var(par_5s);
   all_var(i) = var_3(i)+var_4(i)+var_5(i);
end

%creates matrix that has the Euclidean distance between each - weighted 
m_weighted = zeros(num_cs,num_cs);
for i = 1:num_cs
    for j = 1:num_cs
        m_weighted(i,j) = abs(all_var(i)-all_var(j));
    end
end

%creates matrix that is unweighted. Only connections if difference <= avg/4 
avg_var = mean(all_var);
m_unweighted = zeros(num_cs,num_cs);
for i = 1:num_cs
    for j = 1:num_cs
        if m_weighted(i,j) <= avg_var/4
            m_unweighted(i,j) = 1;
        else 
            m_unweighted(i,j) = 0;
        end
        if i == j
            m_unweighted(i,j) = 0;
        end
    end
end

